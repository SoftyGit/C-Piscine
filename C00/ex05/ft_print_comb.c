#include <unistd.h>

void	ft_print_comb(void)
{
	char	a[6];

	a[0] = ',';
	a[1] = ' ';
	a[2] = '0';
	a[3] = '1';
	a[4] = '2';
	write(1, a +2, 3);
	a[4]++;
	while (a[2] <= '9')
	{
		while (a[3] <= '9')
		{
			while (a[4] <= '9')
			{
				write(1, a, 5);
				a[4]++;
			}
			a[3]++;
			a[4] = a[3] + 1;
		}
		a[2]++;
		a[3] = a[2] + 1;
		a[4] = a[3] + 1;
	}
}
