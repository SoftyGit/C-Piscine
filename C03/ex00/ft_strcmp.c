int	ft_strcmp(char *s1, char *s2)
{
	while (1)
	{
		if (*s1 != *s2)
		{
			return (*s1 - *s2);
		}
		else
		{
			if (*s1 == 0)
			{
				return (0);
			}
			s1++;
			s2++;
		}
	}
}
