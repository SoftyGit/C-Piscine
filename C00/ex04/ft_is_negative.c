#include <unistd.h>

void	ft_is_negative(int n)
{
	char	wt[4];

	wt[0] = 'N';
	wt[1] = 'P';
	if (n < 0)
	{
		write(1, wt, 1);
	}
	else
	{
		write(1, wt + 1, 1);
	}
}
